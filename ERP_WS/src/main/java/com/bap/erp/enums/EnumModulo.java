package com.bap.erp.enums;

/**
 *
 * @author Jonas
 */
public enum EnumModulo {
    	CUENTAS_POR_COBRAR("CPC"),    	
        CUENTAS_POR_PAGAR("CPP");
    
    private String codigo;
    
    private EnumModulo(String codigo){
        this.codigo = codigo;
    }
    
    
    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
}
